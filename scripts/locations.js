function drawMarkers(){

	var ow = document.getElementById("overworld");
	var ot = document.getElementById("onetwo");
	var tf = document.getElementById("threefour");
	var fs = document.getElementById("fivesix");
	var se = document.getElementById("seveneight");
	var ni = document.getElementById("nine");
	
	for ( i = 0; i < markers.length; i++){
		markers[i].elem = document.createElement("img");
		markers[i].elem.setAttribute("src", "images/marker0.png");
		markers[i].elem.id = "marker " + i;
		markers[i].elem.style.left = markers[i].x + "px";
		markers[i].elem.style.top = markers[i].y + "px";
		markers[i].elem.onclick = toggleMarker;
		//markers[i].elem.onmouseenter = showTooltip;
		//markers[i].elem.onmouseleave = hideTooltip;
		if ( i < 52 ){
			ow.appendChild(markers[i].elem);
			markers[i].elem.setAttribute("class", "owmarker");
		} else if (( i >= 52 ) && ( i <= 75 )){
			ot.appendChild(markers[i].elem);
		} else if (( i >= 76 ) && ( i <= 98)){
			tf.appendChild(markers[i].elem);
		} else if (( i >= 99 ) && ( i <= 125 )){
			fs.appendChild(markers[i].elem);
		} else if (( i >= 126 ) && ( i <= 162 )){
			se.appendChild(markers[i].elem);
		} else if ( i >= 163 ){
			ni.appendChild(markers[i].elem);
		}
		if ( i > 51 ){
			markers[i].elem.setAttribute("class", "lvmarker");
		}
	}
}